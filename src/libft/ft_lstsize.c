#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*temp;
	int		cp;

	temp = lst;
	cp = 0;
	while (temp)
	{
		temp = temp->next;
		cp++;
	}
	return (cp);
}
